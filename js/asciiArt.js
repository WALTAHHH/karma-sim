// Select birth art based on context
export function selectBirthArt(life) {
    const era = life.era?.id || 'contemporary';
    const setting = life.birthContext?.setting || 'urban';
    const wealth = life.wealth || 3;

    // Check for active world events (war)
    if (life.worldEvents && life.worldEvents.length > 0) {
        const hasWar = life.worldEvents.some(e =>
            e.name.toLowerCase().includes('war') ||
            e.name.toLowerCase().includes('conflict') ||
            e.name.toLowerCase().includes('revolution')
        );
        if (hasWar) {
            return birthArt.wartime;
        }
    }

    // Wealth-based selection for extremes
    if (wealth >= 5) {
        return birthArt.wealthy;
    }
    if (wealth <= 1) {
        return birthArt.poor;
    }

    // Era-specific selection
    if (era === 'pre_industrial') {
        return setting === 'rural' ? birthArt.pre_industrial_rural : birthArt.pre_industrial;
    }

    if (era === 'industrial_revolution') {
        return setting === 'urban' ? birthArt.industrial_urban : birthArt.industrial_rural;
    }

    // Contemporary
    if (setting === 'urban') {
        return birthArt.contemporary_urban;
    } else if (setting === 'rural') {
        return birthArt.contemporary_rural;
    } else if (setting === 'suburban') {
        return birthArt.contemporary_suburban;
    }

    return birthArt.default;
}

// Select death art based on context
export function selectDeathArt(life) {
    const health = life.health || 3;
    const wealth = life.wealth || 3;
    const connections = life.connections || 3;
    const age = life.age || 70;

    // Priority: tragic young death
    if (age && age < 40) {
        return deathArt.tragic;
    }

    // Health circumstances
    if (health <= 1) {
        return deathArt.illness;
    }

    if (health >= 4) {
        return deathArt.peaceful;
    }

    // Social/wealth
    if (connections >= 4) {
        return deathArt.connected;
    }

    if (connections <= 1) {
        return deathArt.lonely;
    }

    if (wealth >= 4) {
        return deathArt.wealthy;
    }

    if (wealth <= 1) {
        return deathArt.poor;
    }

    return deathArt.default;
}

// Select event art based on event type or archetype
export function selectEventArt(event) {
    if (!event) return eventArt.default;

    // Check for special migration events
    if (event.special === 'migration' || event.id?.includes('migration') || event.id?.includes('abroad')) {
        return eventArt.migration;
    }

    // Check for trade-off archetype
    if (event.tradeoff) {
        const archetype = event.tradeoff.archetype;

        if (archetype === 'money_health') {
            return Math.random() > 0.5 ? eventArt.wealth : eventArt.health;
        }
        if (archetype === 'passion_safety' || archetype === 'stability_opportunity') {
            return eventArt.crossroads;
        }
        if (archetype === 'family_mobility' || archetype === 'self_others') {
            return eventArt.family;
        }
    }

    // Check stage
    if (event.stage === 'childhood') {
        return eventArt.education;
    }

    return eventArt.crossroads;
}

// Select revelation art for hidden costs
export function selectRevelationArt() {
    return eventArt.revelation;
}

// Select mood art based on era and events
export function selectMoodArt(era, worldEvents = []) {
    // Check for war events
    if (worldEvents.length > 0) {
        const hasWar = worldEvents.some(e =>
            e.name.toLowerCase().includes('war') ||
            e.name.toLowerCase().includes('conflict')
        );
        if (hasWar) {
            return moodArt.war;
        }
    }

    // Era-based mood
    if (era === 'industrial_revolution') {
        return moodArt.industrial;
    }

    if (era === 'contemporary') {
        return moodArt.city;
    }

    return moodArt.peace;
}

// Get all art for a specific category
export function getArtCategory(category) {
    switch (category) {
        case 'birth': return birthArt;
        case 'death': return deathArt;
        case 'event': return eventArt;
        case 'mood': return moodArt;
        default: return eventArt;
    }
}
